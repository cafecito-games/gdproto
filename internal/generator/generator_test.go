package generator_test

import (
	"strings"
	"testing"

	"github.com/cafecito-games/gogdproto/internal/ast"
	"github.com/cafecito-games/gogdproto/internal/generator"
)

func TestGenerateEmptyProto(t *testing.T) {
	file := &ast.ProtoFile{Syntax: "proto3"}
	cls, err := generator.Generate(file, "example.proto")
	if err != nil {
		t.Fatalf("Generate returned error: %v", err)
	}
	if cls == nil {
		t.Fatal("Generate returned nil ClassDefinition")
	}
	if cls.ClassNameDirective != "" {
		t.Errorf("ClassNameDirective = %q, want empty (no class_name directive)", cls.ClassNameDirective)
	}
	if cls.Extends != "RefCounted" {
		t.Errorf("Extends = %q, want %q", cls.Extends, "RefCounted")
	}
	out := cls.ToGDScript(0)
	wantPrefix := "# Generated by gdproto\n# Source: example.proto\n# DO NOT EDIT\n\n\nextends RefCounted\n"
	if !strings.HasPrefix(out, wantPrefix) {
		t.Errorf("output does not start with expected header; got first 200 chars:\n%q", out[:minInt(len(out), 200)])
	}
	if !strings.Contains(out, "enum PB_ERR {") {
		t.Errorf("output missing PB_ERR enum")
	}
	if !strings.Contains(out, "const PROTO_VERSION: int = 3") {
		t.Errorf("output missing PROTO_VERSION constant")
	}
}

func TestGenerateHeaderUsesBasename(t *testing.T) {
	file := &ast.ProtoFile{Syntax: "proto3"}
	cls, err := generator.Generate(file, "/tmp/foo/bar_baz.proto")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	out := cls.ToGDScript(0)
	if !strings.Contains(out, "# Source: bar_baz.proto") {
		t.Errorf("output should reference basename of input path; got first 200 chars:\n%q", out[:minInt(len(out), 200)])
	}
}

func TestGenerateTopLevelEnum(t *testing.T) {
	file := &ast.ProtoFile{
		Syntax: "proto3",
		Enums: []*ast.Enum{{
			Name: "PlayerStatus",
			Values: []*ast.EnumValue{
				{Name: "OFFLINE", Number: 0},
				{Name: "ONLINE", Number: 1},
				{Name: "AWAY", Number: 2},
				{Name: "IN_GAME", Number: 3},
			},
		}},
	}
	cls, err := generator.Generate(file, "example.proto")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	out := cls.ToGDScript(0)
	if !strings.Contains(out, "# Enums") {
		t.Errorf("output missing Enums section header")
	}
	if !strings.Contains(out, "enum PlayerStatus {") {
		t.Errorf("output missing PlayerStatus enum")
	}
	for _, want := range []string{"OFFLINE = 0", "ONLINE = 1", "AWAY = 2", "IN_GAME = 3"} {
		if !strings.Contains(out, want) {
			t.Errorf("output missing enum entry %q", want)
		}
	}
}

func TestGenerateMessageClassShell(t *testing.T) {
	file := &ast.ProtoFile{
		Syntax: "proto3",
		Messages: []*ast.Message{{
			Name: "Player",
			Fields: []*ast.Field{
				{FieldType: "string", Name: "username", Number: 1},
				{FieldType: "int32", Name: "level", Number: 2},
				{FieldType: "string", Name: "inventory", Number: 5, Repeated: true},
			},
			NestedMessages: []*ast.Message{{
				Name: "Position",
				Fields: []*ast.Field{
					{FieldType: "float", Name: "x", Number: 1},
				},
			}},
		}},
	}
	cls, err := generator.Generate(file, "example.proto")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}
	out := cls.ToGDScript(0)
	for _, want := range []string{
		"class Player extends RefCounted:",
		"class Position extends RefCounted:",
		`var _username: String = ""`,
		"var _level: int = 0",
		"var _inventory: Array[String] = []",
		"var _x: float = 0.0",
		"func set_username(value: String) -> void:",
		"func get_username() -> String:",
		"func add_inventory(value: String) -> void:",
	} {
		if !strings.Contains(out, want) {
			t.Errorf("output missing fragment %q", want)
		}
	}
}

func minInt(a, b int) int {
	if a < b {
		return a
	}
	return b
}
